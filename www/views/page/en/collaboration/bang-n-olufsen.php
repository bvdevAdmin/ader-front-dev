				if(img_eq < $(this).find("ul > li").length && $(this).find("ul > li").eq(img_eq).hasClass("on") == false) {
					$(this).find("ul > li.on").removeClass("on");
					$(this).find("ul > li").eq(img_eq).addClass("on");
