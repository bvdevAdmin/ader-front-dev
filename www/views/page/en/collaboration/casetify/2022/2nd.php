<script>

window.addEventListener("load", function () {
    if($("article.casetify-2nd").length > 0){

        var swipermodel = document.querySelector("#swiper-model")?.swiper;

        if (swipermodel) {
            swipermodel.on("slideChange", function () {
                $(".thumb #thumb-list > li").removeClass("on");
                $(".thumb #thumb-list > li#thumb-" + (this.realIndex + 1)).addClass("on");
            });
        }

        $(".thumb #thumb-list > li").click(function() {
            $(".thumb #thumb-list > li").removeClass("on");
            $(this).addClass("on");
            var list_id = $(this).attr("id");
            var list_idx = list_id.replace(/[^0-9]/g, "");
            if(swipermodel) {
                swipermodel.slideTo(list_idx - 1);
            }
        });	
    }
});

</script>