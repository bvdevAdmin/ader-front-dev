$("article.kitsune .list.mo > .nav-product > ul > li").click(function() {
    $('article.kitsune .list.mo > .nav-product > ul > li').removeClass("on");
    $(this).addClass("on");
    $('.list > .product > ul').removeClass("on");
    $(".goods-list-" + $(this).index()).addClass("on");
    $('html,body').animate({scrollTop : $("article.kitsune .list.mo").offset().top - 50}, 400);
});

$(window).scroll(function() {
    var navHeight = $(".list.mo > .nav-product").height();
    
    $("article.kitsune .list.mo > .product > ul").each(function() {						
        let offset = $(".list.mo").offset().top - 50;
        let currentTop = $(window).scrollTop();
        let calc = $("article.campaign").offset().top - navHeight - 50 - currentTop;

        // 현재 스크롤이 list.offset().top에 걸렸을때
        if(currentTop >= offset) {
            $(".list.mo > .nav-product").addClass('on');
            //현재 스크롤이 ul 영역을지나 하단 campaign.offset().top에 닿았을때
            if($(window).scrollTop() > $("article.campaign").offset().top - navHeight - 50){
                $(".list.mo .nav-product").css({marginTop:calc + "px"});
            }
            else {
                $('.list.mo > .nav-product').removeAttr("style");
            }
        }
        else {
            $('.list.mo > .nav-product').removeClass('on');
            $('.list.mo > .nav-product').removeAttr("style");
        }
    });
});

</script>