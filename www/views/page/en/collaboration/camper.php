function camperScroll() {
    $("article.list > .product > .gallery").each(function() {
        let calc = $("#second").offset().top - $(window).scrollTop() - 300;
        // var Top = $("main.camper section.media").height() + 525;
        var Top = $("main.camper section.media").height() + 550;
        //gallery 걸렸을때
        if($(window).scrollTop() >= Top) {
            $("article.list .gallery > .cont-2").addClass("on");
            if(calc <= 0) {
                $("article.list .gallery > .cont-2").css({marginTop:calc + "px"});
            }
        }
        //gallery 벗어날때
        else {
            $("article.list .gallery > .cont-2").removeClass('on');
        }
    })
}

function handleResize() {
    if($(window).width() > 1024) {
        if(!$.fn.scrollEvent || $.fn.scrollEvent !== camperScroll) {
            $(window).off("scroll.scrollEvent");
            $(window).on("scroll.scrollEvent", camperScroll);
            $.fn.scrollEvent = camperScroll;
        }
    } 
    else {
        $(window).off("scroll.scrollEvent");
        $.fn.scrollEvent = null;
        $("article.list .gallery > .cont-2").removeClass("on").css({ marginTop: "0px" });
    }
}

handleResize();
$(window).on('resize', handleResize);

$(document).ready(function () {
  var sideNav = $(".side-nav");
  var sideNavHeight = sideNav.height();
  var articleListTop = $("article.list").offset().top;
  // var footer = $("footer");
  var footer = $("#mo_fifth");

  $(window).scroll(function () {
    var scrollTop = $(window).scrollTop();
    var windowHeight = $(window).height();
    // var footerTop = footer.offset().top;
    var footerTop = footer.offset().top;
    // var offsetBottom = footerTop - scrollTop - sideNavHeight - windowHeight + 300;
    var offsetBottom = footerTop - scrollTop - sideNavHeight - windowHeight + 600;
    
    // article.list 아래로 지나갈 때
    if (scrollTop >= articleListTop) {
      sideNav.addClass("on");

      $(".product > div").each(function (index) {
        var sectionTop = $(this).offset().top - sideNavHeight;
        if (scrollTop >= sectionTop) {
          $(".side-nav span").removeClass("on");
          $(".side-nav span").eq(index).addClass("on");
        }
      });

      // footer까지 내려오면 고정
      if (offsetBottom <= 0) {
        sideNav.css({ marginTop: offsetBottom + "px" });
      } else {
        sideNav.css({ marginTop: "" });
      }
    } 
    // article.list 위로 벗어나면 초기화
    else {
      sideNav.removeClass("on").css({ marginTop: "" });
    }
  });
});

</script>