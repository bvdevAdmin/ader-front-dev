							창의적 실마리를 기반으로 크리에이티브가 가진 힘을 캡슐 컬렉션으로 확장하여 선보이고자 합니다.<br><br>
							‘Blue Easter Egg’는 아더에러를 상징하는 블루와 이야기 속 숨겨진 실마리를 뜻하는 이스터 에그의 조합으로,<br>
